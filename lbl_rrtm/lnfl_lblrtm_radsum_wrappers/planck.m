%This function will produce the Planck function radiance in 
%W/m^2/sr/cm^-1

s_identify = 'planck.m';

h = 6.626068e-34; %Planck's constant (J-s)
c = 2.99792458e8;  %speed of light (m/s)
k = 8.314/6.022e23; %Boltzmann's constant (J/K)

